package com.github.klikli_dev.occultism.network;

import com.github.klikli_dev.occultism.Occultism;
import net.minecraft.client.Minecraft;
import net.minecraft.entity.player.EntityPlayer;
import net.minecraft.entity.player.EntityPlayerMP;
import net.minecraft.server.MinecraftServer;
import net.minecraftforge.fml.common.network.simpleimpl.IMessage;
import net.minecraftforge.fml.common.network.simpleimpl.IMessageHandler;
import net.minecraftforge.fml.common.network.simpleimpl.MessageContext;

public abstract class MessageBase<T extends MessageBase<T>> implements IMessage, IMessageHandler<T, IMessage> {

    //region Initialization
    public MessageBase() {

    }
    //endregion Initialization

    //region Overrides
    public IMessage onMessage(T message, MessageContext messageContext) {
        Occultism.proxy.handleMessage(message, messageContext);
        return null;
    }
    //endregion Overrides

    //region Methods
    public abstract void onClientReceived(Minecraft minecraft, T message, EntityPlayer player, MessageContext context);


    public abstract void onServerReceived(MinecraftServer minecraftServer, T message, EntityPlayerMP player,
                                          MessageContext context);
    //endregion Methods
}
